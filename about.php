                <span class="faq1">About Us</span><br />
                <span class="faq2">Okhati, Inc. is a registered non-profit organization that aims to promote, facilitate, and improve the healthcare system of Nepal through information technology.</p>
          <h4> <em>Okhati believes in accessibility.</em></h4>
          <p>Accurate, actionable, and easily accessible information are paramount to improving Nepal’s healthcare system. With the interests of patients, caregivers, healthcare professionals, and policymakers in mind, Okhati provides multiple platforms for information integration and dissemination (Ask a Doctor, Okhati Map, Okhati Innovation).</p>
          <h4> <em>Okhati believes in collaboration.</em></h4>
          <p>Collaboration among the different stakeholders will improve the efficiency, accountability and outcome of the health system of Nepal. In addition, public participation and patient engagement will help generate relevant and actionable information. Okhati empowers these stakeholders with opportunities to participate in meaningful collaboration:</p>
                <ul>
                <li>Ask a Doctor engages healthcare providers and patients</li>
                <li>Okhati Map engages public, policymakers, and administrators</li>
                <li>Okhati Innovation engages programmers and developers</li>
                </ul>
                <h4><em><span style="line-height: 1.5em;">Mission:</span></em></h4>
                <p><span style="line-height: 1.5em;">Okhati aims to improve Nepal’s healthcare system by creating a platform that facilitates the accessibility of actionable information to the public, healthcare professionals, and policymakers.</span></p>
          <h4> <em><span style="line-height: 1.5em;">Vision:</span></em></h4>
          <p> To improve the efficiency of Nepal’s healthcare system through the integration of technology, network and public participation.</p>
                <blockquote><p><span style="line-height: 1.5em;"><em>Create:</em> Create a platform to innovate and improve the healthcare system.</span></p>
                <p><span style="line-height: 1.5em;"><em>Connect:</em> Connect the the stakeholders of the system to foster collaboration.  </span></p>
                <p><em>Care:</em> Increase access to actionable healthcare information to improve health outcome and quality of care.</p>
                </blockquote>
